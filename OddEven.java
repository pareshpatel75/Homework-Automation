package com.Homework;

public class OddEven {
    // Odd Or Even by using % Operator

    public static void main(String[] args) {
        int number = 101;

        if (number % 2 == 0) {
            System.out.println("Number is Even");
        }
        else if (number % 2 == 1) {
            System.out.println("Number is Odd");
        }
    }
}
