package com.Homework;

public class Unary {
    //Unary operator Increment and Decrement operator
    public static void main(String[] args) {

        int a = 10;

        System.out.println(a++);//print10
        System.out.println(a++);//print11
        System.out.println(a++);//print12
        System.out.println(a++);//print13
        System.out.println(a++);//print14
        System.out.println(a++);//print15
        System.out.println(a++);//print16
        System.out.println(a++);//print17
        System.out.println(a++);//print18
        System.out.println(a++);//print19
        System.out.println(a++);//print20

        System.out.println("----------");

        System.out.println(a--);//print21
        System.out.println(a--);//print20
        System.out.println(a--);//print19
        System.out.println(a--);//print18
        System.out.println(a--);//print17
        System.out.println(a--);//print16
        System.out.println(a--);//print15
        System.out.println(a--);//print14
        System.out.println(a--);//print13
        System.out.println(a--);//print12
        System.out.println(a--);//print11


    }
}