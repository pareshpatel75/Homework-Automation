package com.Homework;

public class Unary {
    public static void main(String[] args) {

        int a = 23;
        boolean b= true;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(!b);
    }
}
